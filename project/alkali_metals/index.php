	<h1>Alkali Metals</h1>
	<p>Alkali metals belong to the s-block elements occupying the leftmost side of the periodic table. Alkali metals readily lose electrons, making them count among the most reactive elements on earth. In this article, we will explain the electronic configurations, ionization enthalpy, hydration enthalpy and atomic, ionic radii and other physical and chemical properties of the group one alkali metals.</p>
	<h1>What are Alkali Metals?</h1>
	<p>In general ‘alkali’ refers to the basic or alkaline nature of their metal hydroxides. The compounds are called alkali metals because when they react with water they usually form alkalies which are nothing but strong bases that can easily neutralize acids.</p>
	<p>Alkali metals have a corresponding [Noble gas] ns1 electronic configuration. They occupy the first column of the periodic table. Alkali elements are Lithium(Li), Sodium(Na), Potassium (K), Rubidium (Ru), Cesium (Cs) and Francium (Fr) occupying successive periods from first to seven. Francium is a radioactive element with very low half-life.</p><p>However, the main reason why hydrogen (H) is not considered as an alkali metal is that it is mostly found as a gas when the temperature and pressure are normal. Hydrogen can show properties or transform into an alkali metal when it is exposed to extremely high pressure.</p>

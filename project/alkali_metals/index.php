	<h1>Electronic Configuration of Alkali Metals</h1>
	<li>Alkali metals have one electron in their valence shell.</li>
	<li>The electronic configuration is given by ns1. For example, the electronic configuration of lithium is given by 1ns<sub>1</sub> 2ns<sub>1</sub>.</li>
	<li>They tend to lose the outer shell electron to form cations with charge +1 (monovalent ions).</li>
	<li>This makes them the most electropositive elementsand due to the same reason, they are not found in the pure state.</li>
	<h1>Atomic and Ionic Radii of Elements</h1>
	<p>Atomic and ionic radii of elements increase, regularly down the column. Also, every alkali metal has the largest radii than any other element in the corresponding period.</p>

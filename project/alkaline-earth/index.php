	<p>Fluorides are insoluble in water. The solubility of other halides decreases with increase in ionic size i.e. from Mg<sup>2+</sup> to Ba<sup>2+</sup>. Halides are hygroscopic and have the water of crystallization in their solid state (CaCl<sub>2</sub>.6H<sub>2</sub>O). Fused halides are used as dehydrating agents.</p>
	<h1>Sulphates</h1>
	<p>Contrary to alkali metal sulphates, beryllium sulphate is water-soluble. The smaller size and the charge density increases the hydration energy of the beryllium sulphate leading to more solubility. In other sulphates, increasing lattice energy and the decreasing hydration energy (due to increasing size) decreases their solubility form BeSO<sub>4</sub> to BaSO<sub>4</sub>.</p>

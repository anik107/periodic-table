	<h1>Alkaline earth Metals</h1>
	<p>The alkaline earth metals are the elements that correspond to group 2 of the modern periodic table. This group of elements includes beryllium, magnesium, calcium, strontium, barium, and radium. The elements of this group are quite similar in their physical and chemical properties. For example, all alkaline earth metals are silvery-white coloured solids under standard conditions. They are also highly lustrous (shiny) and are quite reactive. The general electronic configuration of these elements is ns2. Since the alkaline earth metals have a completely full s-orbital in their respective valence shells, they tend to readily lose two electrons to form cations with a charge of +2. Thus, the most common oxidation state exhibited by the alkaline earth metals is +2.</p>
	<h1>What are Alkaline Earth Metals?</h1>
	<p>Elements whose atoms have their s-subshell filled with their two valence electrons are called alkaline earth metals. Their general electronic configuration is [Noble gas] ns<sup>2</sup>.</p>
	<h3>Examples of Alkaline earth Metals:</h3><p>Beryllium (Be), Magnesium(Mg), Calcium (Ca), Strontium (Sr), Barium(Ba) and Radium (Ra).</p>

	<h1>Physical Properties of Alkaline Earth Metals</h1>
	<p>Down the column, nuclear charge increases and a new orbital is added to each alkaline earth atom.Alkaline earth elements can lose both s-electrons and hence become doubly positive cationic. The cationic radius is smaller than the neutral atom. Still, the ionic radii increase down the column.</p>
	<h3>For Example: R<sub>Be</sub> ˂ R<sub>Mg</sub> ˂ R<sub>Ca</sub> ˂ R<sub>Sr</sub> ˂ R<sub>Ba</sub> and R<sub>Be</sub><sup>2+</sup> ˂ R<sub>Mg</sub><sub>2+</sub> ˂ R<sub>Ca</sub><sup>2+</sup> ˂ R<sub>Sr</sub><sup>2+</sup> ˂ R<sub>Ba</sub><sup>2+</sup></h3>
	<h1>Why Alkaline Earth Metals are Denser than Alkali Metals?</h1>
	<p>Radii being smaller, the volume of the atoms are also smaller. In addition, due to the presence of two valence electrons, atoms have stronger metallic bonding. Hence, alkaline earth metals have more density and harder than alkali metals.</p>
	<p>Density generally increases from magnesium to radium while calcium has the lowest density among the alkaline earth metals.</p>
	<h1>Why Solubility of Alkaline Earth Metals decreases down the Group?</h1>
	<p>Beryllium ion is the most soluble and the solubility decreases with increasing size so that Barium ion is the least water-soluble alkaline earth metal ion. Solubility in water is related to the ionic nature and size.</p>
	<p>Smaller ions have higher charge density and can be solvated by more water molecules. This releases a higher enthalpy of hydration and makes the hydrated ions more stable.</p>
	<p>Example: Solubility of Be<sup>2+</sup> > Solubility of Mg<sup>2+</sup> > Solubility of Ca<sup>2+</sup> > Solubility of Sr<sup>2+</sup> > Solubility of Ba<sup>2+</sup></p>
	<h1>Chemical Properties of Alkaline Earth Metals</h1>
	<p>The key features of the compounds of alkaline earth metals and their general characteristics are discussed in this subsection.</p>
	<h1>Hydrides</h1>
	<p>Beryllium does not react with hydrogen directly. Beryllium hydride can be prepared by the reduction of beryllium chloride with lithium aluminium hydride.</p>

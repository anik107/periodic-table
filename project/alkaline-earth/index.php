	<h1>Halides</h1>
	<p>Alkaline earth metals from calcium to barium react with all halogens to form solid ionic halides with a definite crystal structure. Reactivity decreases from fluorine to iodine. Beryllium halides are an exception with more covalent bonding because of the high polarization of the small covalent ion on the electron cloud of the halogen anion as indicated by the <a href="https://en.wikipedia.org/wiki/Fajans%27_rules">Fajan’s rule</a>.</p>
	<p>In the gas phase, Beryllium halides exist as individual molecules and in the solid phase, they form chains of Be-X.</p>
	<center>
		<img src="images2.png">

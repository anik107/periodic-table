	<h1>What are D Block Elements?</h1>
	<p>Elements having electrons (1 to 10) present in the d-orbital of the penultimate energy level and in the outermost ‘s’ orbital (1-2) are d block elements. Although electrons do not fill up ‘d’ orbital in the group 12 metals, their chemistry is similar in many ways to that of the preceding groups, and so considered as d block elements.</p>
	<p>These elements typically display metallic qualities such as malleability and ductility, high values of electrical conductivity and thermal conductivity and good tensile strength. There are four series in the d block corresponding to the filling up of 3d, 4d, 5d or 6d orbitals.</p>
	<ul>
		<li><p>3d- Sc, Ti, V, Cr, Mn, Fe, Co, Ni, Cu, Zn</p></li>
		<li><p>4d- Y, Zr, Nb, Mo, Tc, Ru, Rh, Pd, Ag, Cd</p></li>
		<li><p>5d- La, Hf, Ta, W, Re, Os, Ir, Pt, Au, Hg</p></li>
		<li><p>6d- incomplete.</p></li>
	</ul>
	<p>There are 10 elements filling up the ‘d’ orbital in each series.</p>
	<h1>Position of D Block Elements in Periodic Table</h1>
	<center>
		<img src="d_block.jpg">
	</center>
	<p>D block elements, occupy columns 3 to 12 and may have atoms of elements with completely filled ‘d’ orbital. IUPAC defines a transition metal as “an element whose atom or its cations has a partially filled d sub-shell.</p>
	<h1>Why D Block Elements are called Transition Elements?</h1>
	<p>Transition elements occupy groups 4–11. Scandium and yttrium of group 3, having a partially filled d subshell in the metallic state also are considered as transitions elements. Elements like Zn, Cd and Hg of the 12 columns of the d block have completely filled d-orbital and hence are not considered as transition elements.</p>
	<p>Transition Elements are so named, indicating their positioning and transition of properties between, s and p block elements. So, all the transition metals are d block elements but all d block elements are not transition elements.</p>
	<h1>Properties of D Block Elements</h1>
	<h1>Ionization Energy of D Block Elements</h1>
	<center>
		<img src="ionization-energy-of-d-block-elements.jpeg">
	</center>
	<p>Ionization energy is the energy needed to remove the valence electron from the atom/ion and is directly related to the force of attraction on the electron. Hence, larger the nuclear charge and smaller the radii of the electron larger will be the ionization energy (IE). Ionization Energy also will be more for half-filled and fully filled orbitals.
	<p>Ionization Energy of the d block elements is larger than s-block and smaller than the p-block elements, between which, they are placed. In the first series, except chromium and copper first Ionization Energy involves removal from filled s-orbital. Among them, the ionization energy of d block elements increases with the increase in atomic number up to Fe.</p>
	<p>In Co and Ni, increasing sharing of d-electrons compensate for the atomic number increase resulting in the decrease of Ionization Energy. Copper and zinc show increasing IE, as s -block elements. In the second series, elements from Niobium have single electrons in the s-orbital.</p>
	<p>Hence, they show a gradual increase in IE with increasing atomic number. Palladium, on the other hand, has a completed d-shell and no electron in the s-shell. So, Pd shows the maximum IE. Because of lanthanide contraction, the attraction of electrons by the nuclear charge is much higher and hence IE of 5d elements are much larger than 4d and 3d. In 5d series, all elements except Pt and Au have filled s-shell.Elements from Hafnium to rhenium have the same IE and after IE increases with the number of shared d-electrons such that Iridium and Gold have the maximum IE.</p>
	<h1>Oxidation States of D Block Elements</h1>
	<p>Oxidation state is a hypothetical state, where the atom appears to release or gain electrons more than the usual valency state. It is still useful in explaining the properties of the atom/ion. Transition elements/ions may have electrons in both s and d-orbitals.</p>
	<p>Each transition element can hence exhibit a minimum oxidation state corresponding to the number of s-electrons and maximum oxidation state equivalent to the total number of electrons available in both s and d-orbitals. In between oxidation states also become possible.</p>

	<h1>Physical Properties of D Block Elements</h1>
	<p><b>Density:</b> Among the transition series, the trend in density will be reverse of atomic radii, i.e. density increase remains almost the same and then decreases along the period.</p>
	<center>
		<img src="R.png">
	</center>
	<p>Down the column density of 4d series is larger than 3d. Due to lanthanide contraction and a larger decrease in atomic radii and hence, the volume density of 5d series transition elements are double than 4d series.</p>
	<p>In the 3d series, scandium has the lowest density and copper highest density. Osmium (d=22.57g cm-3) and Iridium (d=22.61g cm-3) of 5d series have the highest density among all d block elements.</p>
	<p>Some relative radii of d block elements are Fe ˂ Ni ˂ Cu, Fe ˂ Cu ˂ Au, Fe ˂ Hg ˂ Au.</p>
	<h1>Magnetic Properties of D Block Elements</h1>
	<p>Materials are classified by their interaction with the magnetic field as:</p>
	<ul>
		<li><p><b>Diamagnetic: </b>if repelled,</p></li>
		<li><p><b>Paramagnetic:  </b>if attracted and</p></li>
		<li><p><b>Ferromagnetic: </b>if it can retain the larger magnetic nature even in the absence of a magnetic field.</p></li>
	</ul>
	<p>Paired electrons cause diamagnetism. Unpaired electrons result in para-magnetism and aligned together unpaired electrons produce ferromagnetism. D block elements and their ions exhibit this behaviour depending on the unpaired electrons.</p>

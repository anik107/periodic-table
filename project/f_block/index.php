	<h1>What are F Block Elements?</h1>
	<p>Elements whose f orbital getting filled up by electrons are called f block elements. These elements have electrons, (1 to 14) in the f orbital, (0 to 1) in the d orbital of the penultimate energy level and in the outermost’s orbital.</p>
	<p>There are two series in the f block corresponding to the filling up of 4f and 5f orbitals. The elements are 4f series of Ce to Lu and 5f series of Th to Lw. There are 14 elements filling up the ‘f’ orbital in each series.</p>
	<center>
		<img src="f-block-elements.png">
	</center>
	<p><b>The position of F Block Elements in the Periodic Table:</b> F block elements are placed separately at the bottom of the periodic table. They are a subset of 6th and 7th periods.</p>

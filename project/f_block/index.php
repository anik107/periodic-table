	<h1>Properties of F block Elements</h1>
	<ul>
		<li><p>Have electrons added to the ‘f’ sub-orbitals of (n-2) level</p></li>
		<li><p>Are placed between (n-1)d and ns block elements in the periodic table</p></li>
		<li><p>Properties are similar to d-block elements.</p></li>
	</ul>
	<h1>Difference between Lanthanides and Actinides</h1>
	<ul>
		<li><p>Lanthanoids are involved in the filling of 4f- orbitals whereas actinoids are involved in the filling of 5f-orbitals. The binding energy of 4f electrons is comparatively less than that of 5f-electrons. The shielding effect of 5f-electrons is less effective as compared to that of 4f-electrons.</p></li>
		<li><p>The paramagnetic properties of lanthanoids can be easily explained but this explanation is difficult in the case of actinoids.</p></li>
		<li><p>Lanthanides are non-radioactive in nature except promethium whereas all actinide series elements are radioactive.</p></li>
		<li><p>Lanthanides do not have a tendency to form oxo-cations, but several oxo-cations of actinide series exist. The compounds formed by lanthanides are less basic on the other hand the compounds of actinides are highly basic.</p></li>
	</ul>

	<h1>What Are P-Block Elements?</h1>
	<p>The Periodic Table is a chart which includes all the known elements according to their atomic numbers. Different columns in the periodic table are known as groups and rows are called as periods. The elements which are placed left to the periodic table are metals and elements which are on the right are known as non-metals. All the metals are solid at room temperature and non-metals are liquid or gaseous. Metalloids include properties of both the metal and non-metals.</p>
	<center>
		<img src="p_block.jpg">
	</center>
	<p>The p-block exists on the right side of the periodic table which includes elements from six columns, starts with IIIA column and ends with VIIIA Column. Helium which is placed at the top of column VIIIA is not included in the p-block. The p-block elements are united as their valence electrons are in p orbital. This orbital contains six-lobed shapes that are coming from a central point at evenly spaced angles. It holds a maximum of 6 electrons and hence there are six columns in the p-block. The first column of p-block where elements in column IIIA have one valence electron. And in column IVA, the 2nd column has two valence electrons. This trend continues till we reach to the column VIIIA which has 6 valence electrons.</p>
	<h1>Properties of P-Block Metals</h1>
	<p>These metals have classical metal characteristics that they are shiny, good conductors of heat and electricity and also they lose electrons. They have high melting points and react with non-metals to form ionic compounds. When ion bonds of a positive metal react with negative non-metal ions, ionic compounds are formed. Gallium is placed in the 4th row of column IIIA is a metal that can easily melt on the palm of your hand. Tin exists in the 5th row of column IVA and is a flexible, abundant and useful metal. It is an important component as many other metal alloys â€“ solder, pewter, and bronze.</p>
	<p>Both alkali and alkaline earth elements show a regular gradation in their properties among their respective group elements. But the first member of both S block elements, namely, Lithium and Beryllium differ much from the rest of their members but at the same time, they resemble more with the diagonal element present in the next column.</p>
	<p>Just next to tin is lead which is a toxic metal along with an interesting history. Lead is used for many things right from food sweeteners to pottery glazes to eating utensils. Lead is toxic and used in the manufacturing of paints.</p>
	<h1>Properties of p-block Metalloids</h1>
	<p>Metalloids include properties of both the metals and nonmetals. Elements that are recognized as metalloids are placed in the p-block such as germanium, silicon, arsenic, and tellurium. Silicon is the popular metalloid and is the second most abundant element on the Earth. Also, used to make microchips for computers and different electronic devices. Germanium includes similar properties to Silicon.</p>
	<h1>Properties of p-block Non-Metal</h1>
	<p>Many of the p-block elements are nonmetals which have low boiling points. These are poor conductors of electricity and do not lose electrons. Some non-metals are solid at room temperature while others are gases. There is one non-metal in the p-block element, bromine which is liquid at room temperature.</p>

	<h1>Chemical Properties of S-Block Elements</h1>
	<h1>Atomic and Ionic Radii</h1>
	<p>When the s block elements of the modern periodic table are observed it is seen that the size of the alkali metals is larger compared to other elements in a particular period. As the atomic number increases the total number of electrons increases along with the addition of shells.</p>
	<p>On moving down the group the atomic number increases. As a result, the atomic and ionic radius of the alkali metals increases.</p>

	<h1>S Block Elements</h1>
	<p>The elements of Group 1 and Group 2 of the modern periodic table are called S-block elements. Two types of s block elements are possible i.e. the elements with one electron (s1) or the elements with two electrons (s2) in their s-subshell.</p>
	<p>S-block comprises 14 elements namely hydrogen (H), lithium (Li), helium (He), sodium (Na), beryllium (Be), potassium (K), magnesium (Mg), rubidium (Rb), calcium (Ca), cesium (Cs), strontium (Sr), francium (Fr), barium (Ba), and radium (Ra).</p>
	<h1>What are S-Block Elements?</h1>
	<p>The s-block elements having only one electron in their s-orbital are called group one or alkali metals whereas the s block elements having two electrons filling their s-orbital are called group two or <a href="http://localhost/project/alkaline-earth">alkaline earth metals.</a></p>
	<p>The electrons present in an atom occupy various sub-orbitals of available energy levels in the order of increasing energy. The last electron of an atom may find itself in either of the s, p, d and f subshells. Accordingly, the elements of the atom having their last valence electron present in the s-suborbital are called the s block elements.</p>

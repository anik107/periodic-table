	<h1>Properties of S-Block Elements</h1>
	<p>Both alkali and alkaline earth elements show a regular gradation in their properties among their respective group elements. But the first member of both S block elements, namely, Lithium and Beryllium differ much from the rest of their members but at the same time, they resemble more with the diagonal element present in the next column.</p>
	<h3>The anomaly of these S-block elements is due to;</h3>
	<ol>
		<li><p>Low atomic and ionic size</p></li>
		<li><p>Greater charge density (charge/volume of the atom)</p></li>
		<li><p>Greater polarization</p></li>
		<li><p>Absence of d-orbitals.</p></li>
	</ol>
	<p>Greater polarization of s block elements makes the first element more covalent and differentiates them from the rest which are ionic.The similarity in size and charge density makes them resemble the element diagonally placed in the next group (diagonal relationship).</p>

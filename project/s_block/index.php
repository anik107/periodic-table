	<h1>Ionization Enthalpy</h1>
	<p>As we go down the group the size of the atoms increases due to which the attraction between the nucleus and the electrons in the outermost shell decreases. As a result, the ionization enthalpy decreases. The ionization enthalpy of the alkali metals is comparatively lesser than other elements.</p>
	<h1>Physical Properties of S-Block elements</h1>
	<ul>
		<li><p>In the S block elements, the density of the alkali metals increases down the group. Exception: the density of potassium is less than the density of sodium.</p></li>
		<li><p>The alkali metals have a low melting and boiling point due to the weak metallic bonding.</p></li>
		<li><p>Alkali metals and their respective salts have the capability to impart colour to the oxidizing flame due to the heat generated from the flame which excites the valence electrons from one energy level to another energy level. This helps in the detection of alkali metals during the flame test.</p></li>
